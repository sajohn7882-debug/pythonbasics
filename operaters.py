# comparison operator
num_1=7
num_2=19
print(num_1 == num_2) #equal to
print(num_1 != num_2) #not equal to
print(num_1 > num_2)  #greater than
print(num_1 < num_2)  #less than
print(num_1 >= num_2) #greater than or equals to
print(num_1 <= num_2) #less than or equals to

